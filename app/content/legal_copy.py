def format_legal_sections(sections: list[dict], **ctx) -> list[dict]:
    out = []
    for section in sections:
        item = {
            "title": section["title"],
            "paragraphs": [p.format(**ctx) for p in section.get("paragraphs", [])],
            "bullets": [b.format(**ctx) for b in section.get("bullets", [])],
            "after": [a.format(**ctx) for a in section.get("after", [])],
        }
        out.append(item)
    return out
