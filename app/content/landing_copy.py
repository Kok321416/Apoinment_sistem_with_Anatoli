def faq_with_support(support_email: str) -> list[dict]:
    return [
        {
            "question": item["question"],
            "answer": item["answer"].format(support_email=support_email),
        }
        for item in FAQ
    ]


def footer_with_context(support_email: str, brand: str, year: int) -> dict:
    columns = []
    for col in FOOTER["columns"]:
        links = []
        for link in col["links"]:
            links.append(
                {
                    "label": link["label"],
                    "href": link["href"].format(support_email=support_email),
                }
            )
        columns.append({"title": col["title"], "links": links})
    return {
        "tagline": FOOTER["tagline"],
        "columns": columns,
        "copyright": FOOTER["copyright"].format(year=year, brand=brand),
    }
